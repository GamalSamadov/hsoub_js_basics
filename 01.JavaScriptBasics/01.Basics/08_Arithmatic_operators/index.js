console.log(8 + 2);
console.log(8 - 2);
console.log(8 * 2);
console.log(8 / 2);

// Reminder
console.log(4 % 2);
console.log(8 % 3);

// Increment & decrement
let x = 4;
console.log(++x); // add first then show
console.log(--x, x); // 4

console.log(x++); // show first then add
console.log(x);
console.log(x--); 

// Concatenation
console.log("Hello" + "world");
console.log('1' + 2);
console.log(2 + "1");
console.log(2 + 2 + '1'); //41